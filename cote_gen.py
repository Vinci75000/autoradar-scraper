def toks(s):
    s = (s or "").lower()
    s = re.sub(r"([a-z])([0-9])", r"\1 \2", s)
    s = re.sub(r"([0-9])([a-z])", r"\1 \2", s)
    return [t for t in re.findall(r"[a-z0-9]+", s) if t]


def clean_toks(s):
    s = (s or "").lower()
    # bruit CO2 "155 g/km", "155g km", "co2 155 g/km" -> retire AVANT tokenisation
    # (sinon le "g" des grammes percute la famille G-Classe)
    s = re.sub(r"co2", " ", s)
    s = re.sub(r"\d+\s*g\s*/?\s*km", " ", s)
    s = re.sub(r"\d*\s*g\s*-?\s*tronic", " ", s)  # 7G/9G-Tronic = boite -> le 'g' percutait la G-Classe
    out = []
    for t in toks(s):
        if t in NOISE:
            continue
        out.append(t)
    return out


def brand_key(brand):
    b = (brand or "").lower().strip()
    b = unicodedata.normalize("NFD", b)
    b = "".join(c for c in b if unicodedata.category(c) != "Mn")
    if "mercedes" in b:
        return "mercedes-benz"
    if "alfa" in b:
        return "alfa romeo"
    if "aston" in b:
        return "aston martin"
    if "land rover" in b or "landrover" in b:
        return "land rover"
    if "chevrolet" in b or b == "chevy":
        return "chevrolet"
    if b in ("vw", "volkswagen ag"):
        return "volkswagen"
    if "range rover" in b:
        return "land rover"
    if "vauxhall" in b:
        return "opel"
    return b


def infer_generation(brand, model, year):
    fams = GENERATIONS.get(brand_key(brand))
    if not fams:
        return None, None
    mt = set(clean_toks(model))
    # candidats : familles dont les tokens sont un sous-ensemble du modele
    subset = [fam for fam in fams if toks(fam) and set(toks(fam)) <= mt]
    if not subset:
        return None, None

    def _longest(pool):
        bf, bl = None, 0
        for fam in pool:
            l = len(toks(fam))
            if l > bl:
                bf, bl = fam, l
        return bf

    # selection ANNEE-AWARE : prefere la famille LA PLUS LONGUE qui COUVRE l'annee.
    # evite qu'une famille longue mais hors-epoque (ex "300 d" W123, "220" Ponton)
    # ecrase la famille de classe correcte (ex "glc"/"c" sur "GLC 300 d"/"C 220" 2020).
    best_fam = None
    if year:
        yr = int(year)
        covering = [f for f in subset
                    if any(y0 <= yr <= y1 for _, y0, y1 in fams[f])]
        if covering:
            best_fam = _longest(covering)
    if best_fam is None:
        best_fam = _longest(subset)
    entries = fams[best_fam]
    # regroupe par base (ex "997" -> 997.1, 997.2)
    bases = {}
    for c, y0, y1 in entries:
        bases.setdefault(c.split(".")[0], []).append((c, y0, y1))
    # base explicite dans le mo -> on resout la sous-phase par l'annee
    present = [b for b in bases if b != "g" and b in mt]
    if present:
        b = sorted(present, key=len, reverse=True)[0]
        subs = bases[b]
        if year:
            for c, y0, y1 in subs:
                if y0 <= int(year) <= y1:
                    return best_fam, c
        return best_fam, (subs[0][0] if len(subs) == 1 else b)
    # sinon inference pure par annee (donne directement la sous-phase)
    if year:
        for c, y0, y1 in entries:
            if y0 <= int(year) <= y1:
                return best_fam, c
    # fallback honnete : une SEULE generation possible -> aucune ambiguite, on tague
    # (modele mono-generation : Espada, F40, Carrera GT, Stratos... meme sans annee)
    codes = {c for c, _, _ in entries}
    if len(codes) == 1:
        return best_fam, entries[0][0]
    return best_fam, None


def canon_str(brand, model, year):
    fam, code = infer_generation(brand, model, year)
    if code and code not in clean_toks(model):
        return "%s (%s)" % (model, code), fam, code
    return model, fam, code


